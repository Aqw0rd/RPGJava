package Framework;

public class LightRay {
    float x;
    float y;
    float xDest;
    float yDest;
    float angle;
    public LightRay(float x, float y, float xDest, float yDest, float angle){
        this.x = x;
        this.y = y;
        this.xDest = xDest;
        this.yDest = yDest;
        this.angle = angle;
    }
}
